import pandas as pd
df=pd.read_excel('data_01.xlsx')

df['半球和'] = df['50主_大小_球数'] + df['50客_大小_球数']
df['全球和'] = df['95主_大小_球数'] + df['95客_大小_球数']
df['半sp跨值'] = df['50大小球'] - df['半球和']  # 100大小球代表半场大小球数据，取消了，因为有些比赛没有半
df['目标'] = df['全球和'] - df['半球和']
df1=df[['目标','半球和','全球和','50主_大小_球数','50客_大小_球数','95主_大小_球数','95客_大小_球数']]
df1.to_excel('111.xlsx')